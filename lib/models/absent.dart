// Class Absent
class Absent {
  
  // Variabel yang dibutuhkan untuk absen atau tidak hadir
  int id;
  String employeeId;
  String type;
  String firstDate;
  String lastDate;
  String note;

  // Method constructor
  Absent({
    this.id = 0,
    this.employeeId,
    this.type,
    this.firstDate,
    this.lastDate,
    this.note
  });

}